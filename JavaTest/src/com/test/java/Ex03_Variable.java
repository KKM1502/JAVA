package com.test.java;

public class Ex03_Variable {
	
	public static void main(String[] args) {
		//	Ex03_Variable.java
		//	-변수, Variable
		//	-변화를 가질 수 있는 데이터
		//	-개발자가 명령어를 사용해서 할당받은 공간의 이름 
		
		//	변수
		//	1.생성하기(선언하기)
		//	-자료형 변수명;
		byte 국어;
		
		//	2.초기화하기
		//	-변수명=값;
		국어 = 100;
		
		//	2.5치환하기
		국어 = 95;
		
		//	3.사용하기(입출력)
		System.out.println(국어);
		
		
	}
	
}

